    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ((evt.getSource() instanceof JButton) && (((JButton) evt.getSource()).equals(jButton1))) {
            User user = new User();
            user.setUsername(jTextField1.getText());
            user.setPassword(jPasswordField1.getText());

            UserDAO userDAO = new UserDAO();
            if (userDAO.checkLogin(user)) {
                if (user.getPosition().equalsIgnoreCase("Ban to chuc")) {
                    (new BTCManagerFrm(user)).setVisible(true);
                    this.dispose();
                } else {
                    if (user.getPosition().equalsIgnoreCase("Nhan vien")) {
                        (new NVManagerFrm(user)).setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "The function of the role " + user.getPosition() + " is under construction!");

                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect username and/or password!");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

